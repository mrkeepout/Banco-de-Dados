    private void btnPesquisarEditoraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarEditoraActionPerformed
        if(txtPesquisaEditoraLivro.getText().equals("")){
            JOptionPane.showMessageDialog(null, "O id da editora deve ser informado!", "Mensagem", JOptionPane.PLAIN_MESSAGE);
        }else{
            long idEditora = Long.parseLong(txtPesquisaEditoraLivro.getText()); //pegando o desejo da pesquisa e fazendo loop na lista
            Editora editoraBusca = new Editora();
            if(editoraBusca.buscaEditora(idEditora) != null){
                Editora editora = editoraBusca.buscaEditora(idEditora);
                txtPesquisaEditoraLivro.setText(editora.getNomeEditora());
                
            }
        }
    }//GEN-LAST:event_btnPesquisarEditoraActionPerformed

    private void btnPesquisarAutorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarAutorActionPerformed
        if(txtPesquisaAutorLivro.getText().equals("")){
            JOptionPane.showMessageDialog(null, "O id do autor deve ser informado!", "Mensagem", JOptionPane.PLAIN_MESSAGE);
        }else{
            long idAutor = Long.parseLong(txtPesquisaAutorLivro.getText()); //pegando o desejo da pesquisa e fazendo loop na lista
            Autor autorBusca = new Autor();
            if(autorBusca.buscaAutor(idAutor) != null){
                Autor autor = autorBusca.buscaAutor(idAutor);
                txtPesquisaAutorLivro.setText(autor.getNomeAutor());
                
            }
        }
    }//GEN-LAST:event_btnPesquisarAutorActionPerformed

